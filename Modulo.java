public class Modulo {

    public static void main(String[] args) {

        Double dvalor1 = 12.79;
        System.out.println(dvalor1);

        double valor1 = (double)dvalor1;
        System.out.println(valor1);

        long valor3 = (long)valor1;
        System.out.println(valor3);

        Byte valor2 = 60;
        System.out.println(valor2);

        byte valor4 = (byte)valor2;
        System.out.println(valor4);

        float valor5 = (float)valor1;
        System.out.println(valor5);

        Integer intnumero1 = 100;
        System.out.println(intnumero1);

        int valor6 = (int)intnumero1;
        System.out.println(valor6);

    }
}
